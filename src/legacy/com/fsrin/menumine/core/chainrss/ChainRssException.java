public class ChainRssException extends Exception {

    public ChainRssException() {
        super();

    }

    public ChainRssException(String message) {
        super(message);

    }

    public ChainRssException(Throwable cause) {
        super(cause);

    }

    public ChainRssException(String message, Throwable cause) {
        super(message, cause);

    }

}