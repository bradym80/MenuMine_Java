package com.fsrin.menumine.common.dao;

/**
 * @author Reid
 */
public class CriteriaBuilderException extends Exception {

    /**
     *  
     */
    public CriteriaBuilderException() {
        super();

    }

    /**
     * @param message
     */
    public CriteriaBuilderException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public CriteriaBuilderException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public CriteriaBuilderException(String message, Throwable cause) {
        super(message, cause);

    }

}