public class ServiceDelegateException extends Exception {

    public ServiceDelegateException() {
        super();

    }

    public ServiceDelegateException(String arg0) {
        super(arg0);

    }

    public ServiceDelegateException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

    public ServiceDelegateException(Throwable arg0) {
        super(arg0);

    }

}