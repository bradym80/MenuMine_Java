package com.fsrin.menumine.messages;

/**
 * @author Reid
 *  
 */
public class PasswordHelpException extends Exception {

    /**
     *  
     */
    public PasswordHelpException() {
        super();
    }

    /**
     * @param arg0
     */
    public PasswordHelpException(String arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     */
    public PasswordHelpException(Throwable arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     * @param arg1
     */
    public PasswordHelpException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}