package com.fsrin.menumine.core.menumine.util;

public class ExcelSupportException extends Exception {

    public ExcelSupportException() {
        super();

    }

    public ExcelSupportException(String arg0) {
        super(arg0);

    }

    public ExcelSupportException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

    public ExcelSupportException(Throwable arg0) {
        super(arg0);

    }

}
